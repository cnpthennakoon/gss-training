<?php

namespace App\Http\Controllers;

use App\TrainingProjectStatus;
use Illuminate\Http\Request;

class TrainingProjectStatusController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $trainingProjectStatus = TrainingProjectStatus::paginate(20);

        return view('master-data.training-project-status.index')->withTrainingProjectStatus($trainingProjectStatus);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('master-data.training-project-status.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required|max:255',
        ]);

        $region = new TrainingProjectStatus();
        $region->name = $request->name;
        $region->save();

        return redirect()->route('training-project-status.index');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit(TrainingProjectStatus $trainingProjectStatus)
    {
        return view('master-data.training-project-status.edit', compact('trainingProjectStatus'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'name' => 'required|max:255',
        ]);



        $trainingProject = TrainingProjectStatus::findOrFail($id);
        $trainingProject->name = $request->name;
        $trainingProject->save();


        return redirect()->route('training-project-status.index');    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
    }
}
